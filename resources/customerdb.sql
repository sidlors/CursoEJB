DROP TABLE CUSTOMER;
DROP TABLE CUSTOMERSERVICES;



CREATE TABLE CUSTOMER(
        CUSTOMER_ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1,INCREMENT BY 1), 
	FIRSTNAME VARCHAR(255),
	LASTNAME VARCHAR(255),
        PREFERRED_ID INTEGER,
        PRIMARY KEY (CUSTOMER_ID)
);

CREATE TABLE CUSTOMERSERVICES(
        CUSTOMRSERVICE_ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1,INCREMENT BY 1),
	PREFERRED_ID INTEGER,
	SERVICE_DESCRIPTION VARCHAR(255),
        PRIMARY KEY (CUSTOMRSERVICE_ID)
);




